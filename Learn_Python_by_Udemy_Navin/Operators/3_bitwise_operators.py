"""
Bitwise Operators
1. Complement (~)
2. And (&)
3. Or (|)
4. XOR (^)
5. Left Shift (<<)
6. Right Shift (>>)
"""

## Complement Operator (tilde ~)
print(~12)

## Bitwise And (&), Bitwise Or (|)
print(12 & 13)
print(12 | 13)
print(25 & 30)

## Bitwise XOR (^)
print(12 ^ 13)
print(25 ^ 30)

## Leftshit (<<)
print(13 << 1)
print(13 >> 2)