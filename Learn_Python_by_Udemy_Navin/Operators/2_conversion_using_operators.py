"""
Conversion of one operator to another operator

Decimal (base 10)
Binary (base 2)
Octal (base 8)
Hexa Decimal (base 16)


"""

# Decimal to Binary
a = 25
print(bin(a))

# Binary to Decimal
print(0b10101)
print(0b11001)

# Decimal to Octal
print(oct(a))

# Octal to Decimal
print(0o32)

# Decimal to Hexa Decimal
print(hex(a))

# Hexa Decimal to Decimal
print(0xf)