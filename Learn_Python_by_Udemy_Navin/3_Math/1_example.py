import math

x = math.sqrt(25)
print(x)

print(math.floor(2.9))  # rounds off to 2
print(math.ceil(2.9))  # rounds off to 3 which is nearest value

print(math.pow(3, 3))  # 3 power 3
print(math.pow(2, 5))  # 2 power 5

# Math Constants
print(math.pi)  # prints pi value
print(math.e)  # prints epsilon value

